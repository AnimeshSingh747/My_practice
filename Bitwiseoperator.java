public class Bitwiseoperator {
    
    public static void main(String[] args) {

        int a = 12;
        int b = 33;

        //bitwise and//
        int result = a & b;
        System.out.println(result);

        //bitwise or//
        int resultor = a | b;
        System.out.println(resultor);

        //bitwise XOR//
        int resultxor = a ^ b;
        System.out.println(resultxor);
    }
}
